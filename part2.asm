ORG 0000H
START:
	SETB P3.3
	SETB P3.4
ONE: 
	CALL DELAY
	MOV A, P2
	MOV P1, A
	CALL DELAY
TWO: 
	CLR P3.3
	CALL DELAY
	CLR A
	MOV A, P2
	MOV P1, A
	CALL DELAY
THREE:
	CLR P3.4
	SETB P3.3
	CALL DELAY
	CLR A
	MOV A, P2
	MOV P1, A
	CALL DELAY
	JMP START
DELAY:
	MOV R0, #45H
	DJNZ R0, $
	RET
END